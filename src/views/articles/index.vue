<template>
  <el-card class="articles">
      <bread-crumb slot="header">
        <template slot="title">文章列表</template>

      </bread-crumb>
      <!-- 表单容器  -->
      <el-form style="padding-left:50px">
          <el-form-item label="文章状态:">
              <!-- 放置一个单选组  文章状态，0-草稿，1-待审核，2-审核通过，3-审核失败，4-已删除，不传为全部-->
              <!-- <el-radio-group v-model="searchForm.status" @change="changeCondition">

                <el-radio :label="5">全部</el-radio>
                <el-radio :label="0">草稿</el-radio>
                <el-radio :label="1">待审核</el-radio>
                <el-radio :label="2">审核通过</el-radio>
                <el-radio  :label="3">审核失败</el-radio>
               </el-radio-group> -->
              <el-radio-group v-model="searchForm.status" >
              <!-- label  -->
                <el-radio :label="5">全部</el-radio>
                <el-radio :label="0">草稿</el-radio>
                <el-radio :label="1">待审核</el-radio>
                <el-radio :label="2">审核通过</el-radio>
                <el-radio  :label="3">审核失败</el-radio>
               </el-radio-group>
               <!-- {{searchForm}} -->
          </el-form-item>
          <el-form-item label="频道列表:">
            <!-- 第一种 监听组件的方式收缩 -->
              <!-- <el-select @change="changeCondition"  placeholder="请选择频道" v-model="searchForm.channel_id ">
                  <el-option v-for='item in channels' :key='item.id' :label="item.name" :value="item.id" ></el-option>
              </el-select> -->
              <el-select placeholder="请选择频道" v-model="searchForm.channel_id ">
                  <el-option v-for='item in channels' :key='item.id' :label="item.name" :value="item.id" ></el-option>
              </el-select>
          </el-form-item>
          <el-form-item label="时间选择:">
              <!-- 日期选择器 -->
              <!-- 第一种 监听组件的方式收缩 -->
              <!-- <el-date-picker  @change="changeCondition" value-format="yyyy-MM-dd" v-model="searchForm.dateRange" type="daterange"></el-date-picker> -->
              <el-date-picker   value-format="yyyy-MM-dd" v-model="searchForm.dateRange" type="daterange"></el-date-picker>
          </el-form-item>
      </el-form>
      <el-row class="total" type="flex" align="middle ">
        <span>共找到{{page.total}}条符合条件的内容</span>
      </el-row>
      <div class="article-item" v-for="item in list" :key="item.id.toString()">
        <!-- 左侧  -->
        <div class="left">
           <img :src="item.cover.images.length? item.cover.images[0] :defaultImg" alt="">
           <div class="info">
              <span>{{item.title}}</span>
              <el-tag :type="item.status | filterType" class="tag">{{item.status | filtersStatus}}</el-tag>
              <span class="data">{{item.pubdate }}</span>
          </div>
        </div>
        <!-- 右侧 -->
        <div class="right">
            <span> <i class="el-icon-edit"> </i>修改</span>
            <span @click="delMaterial(item.id)"><i class="el-icon-delete"></i>删除</span>
        </div>
      </div>
      <el-row type="flex" justify='center' align="middle" style="height:60px">
        <el-pagination background layout='prev,pager,next'
         :total="page.total" :current-page="page.currentPage"
         :page-size="page.pageSize" @current-change="changePage">
        </el-pagination>
      </el-row>
  </el-card>
</template>

<script>
export default {
  data () {
    return {
      searchForm: {
        status: 5, // 默认悬着全部
        channel_id: null, //
        dateRange: [ ] // 日期范围
      },
      channels: [], // 接收频道数据
      list: [],
      defaultImg: require('../../assets/img/default.jpg'), // 默认图片
      page: {
        currentPage: 1,
        pageSize: 10, // 黑发头条后端限制 最低10条=》文章列表
        total: 0

      }
    }
  },
  watch: {
    searchForm: {
      handler: function () {
        // 此时数据已经是最新的了
        // this指向组件实例
        this.changeCondition()// 直接调用条件改变的方法
      },
      deep: true
    }
  },
  filters: {
    filtersStatus (value) {
      switch (value) {
        case 0:
          return '草稿'
        case 1:
          return '待审核'
        case 2:
          return '已发表'
        case 3:
          return '审核失败 '
        default:
          break
      }
    },
    filterType (value) {
      switch (value) {
        case 0:
          return 'warning'
        case 1:
          return 'info'
        case 2:
          return ''
        case 3:
          return 'danger'
        default:
          break
      }
    }
  },

  methods: {
    // 删除文章方法
    delMaterial (id) {
      this.$confirm('是否要删除该文章').then(() => {
        // 调用删除的的接口
        this.$axios({
          method: 'delete',
          url: `/articles/${id.toString()}`
        }).then(result => {
          this.$message({
            type: 'succeess',
            message: '删除成功'
          })
          // 重新拉去数据
          this.getConditionArticle()
        })
      })
    },
    // 改变页码方法
    changePage  (newPage) {
      // console.log(newPage)
      this.page.currentPage = newPage// 最新页码
      this.getConditionArticle()// 调用后去文章数据
    },
    // 改变时间
    changeCondition () {
      this.page.currentPage = 1// 强制页面充值为一
      this.getConditionArticle()// 调用后去文章数据
    },
    getConditionArticle () {
      let params = {
        page: this.page.currentPage, // 当前页面
        per_page: this.page.pageSize, // 每页数量
        status: this.searchForm.status === 5 ? null : this.searchForm.status,
        channel_id: this.searchForm.channel_id,
        begin_pubdate: this.searchForm.dateRange.length ? this.searchForm.dateRange[0] : null,
        end_pubdate: this.searchForm.dateRange.length > 1 ? this.searchForm.dateRange[1] : null
      }
      this.getArticles(params)
    },
    // 获取所有的pindao
    getChannels () {
      this.$axios({
        url: '/channels'
      }).then(result => {
        this.channels = result.data.channels
      })
    },
    // 获取文章 数据
    getArticles (params) {
      this.$axios({
        url: '/articles',
        params // es6 简写
      }).then(result => {
        this.list = result.data.results// 获取文章裂变素具
        this.page.total = result.data.total_count // 总数
      })
    }
  },
  created () {
    this.getChannels()
    this.getArticles({ page: 1, per_page: 10 })// 第一次没传参
  }
}

</script>

